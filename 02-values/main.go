package main

import "fmt"

func main() {
	fmt.Println("go" + "lang")

	fmt.Println("2 + 3 =", 2+3)

	fmt.Println("7.0 / 3 =", 7.0/3)
	fmt.Println("7.0 / 3.0 =", 7.0/3.0)

	fmt.Println("6.0 / 3 =", 6.0/3)
	fmt.Println("6.0 / 3.0 =", 6.0/3.0)

	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!false)

	fmt.Println("True =", true)
	fmt.Println("False =", false)
}
